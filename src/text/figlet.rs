//! FIGlet-style banner text rendering.
//!
//! Bundles the "standard" FIGlet font as a compile-time constant. Each ASCII
//! printable character (32–126) has a glyph definition stored as rows of
//! characters.

use crate::error::Result;

const FIGLET_HEIGHT: usize = 6;

/// Encode the standard FIGlet font glyph for a character.
///
/// Returns a slice of `FIGLET_HEIGHT` rows, or `None` if the character has
/// no glyph definition.
fn glyph(ch: char) -> Option<&'static [&'static str]> {
    let code = ch as u32;
    if !(32..=126).contains(&code) {
        return None;
    }
    Some(STANDARD_FONT[(code - 32) as usize])
}

/// Render text as a FIGlet banner using the standard font.
/// Each line of input text produces a banner; multiple lines are separated by
/// blank rows.
pub fn render_figlet(input: &str) -> Result<String> {
    let lines: Vec<&str> = input.lines().collect();
    // Consider empty if no lines or only whitespace/empty lines
    if lines.is_empty() || lines.iter().all(|l| l.trim().is_empty()) {
        return Err(crate::error::FigoError::MissingFields("text is empty".to_string()));
    }

    let mut output = String::new();
    for (li, line) in lines.iter().enumerate() {
        if li > 0 {
            output.push('\n');
        }
        let chars: Vec<Option<&[&str]>> = line.chars().map(glyph).collect();

        for row in 0..FIGLET_HEIGHT {
            for g in chars.iter().flatten() {
                output.push_str(g[row]);
            }
            // Trim trailing spaces on each row, then add newline.
            let end = output.trim_end_matches(' ').len();
            output.truncate(end);
            output.push('\n');
        }
    }
    Ok(output)
}

