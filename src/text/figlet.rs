#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_figlet_hello() {
        let result = render_figlet("Hi").unwrap();
        assert!(result.contains("_   _ "));
        assert!(result.lines().count() >= FIGLET_HEIGHT);
    }

    #[test]
    fn test_figlet_empty() {
        assert!(render_figlet("").is_err());
        assert!(render_figlet("\n").is_err());
    }
}
